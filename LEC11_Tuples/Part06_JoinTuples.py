# We can join two tuples using + 
tup1 = ("Rakan","Rayan")
tup2 = ("Khalid","Ahmed")
tup3 = tup1 + tup2 
print(tup3)
# There is a way to multiply a tuple for example 
tup3 = tup3 * 2
print(tup3)

